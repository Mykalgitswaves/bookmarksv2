<template>
    <div class="bookclub-header">
        
    </div>
    
    <div class="club-feed">

    </div>

    <div class="club-nav">

    </div>
</template>
<script setup>
import { ref } from 'vue';
import { useRoute } from 'vue-router';
import { db } from '../../../../services/db';
import { urls } from '../../../../services/urls';

/**
 * ----------------------------------------------------------------------------
 * @constants
 * ----------------------------------------------------------------------------
 */

const route = useRoute();
const { user, bookclub } = route.params;
const loaded = ref(false);
let error;
let club;

/**
 * ----------------------------------------------------------------------------
 * @end_of_constants
 * ----------------------------------------------------------------------------
 */



/**
 * ----------------------------------------------------------------------------
 * @functions
 * ----------------------------------------------------------------------------
 */


async function loadBookClub() {
    db.get(urls.bookclubs.getClubFeed(bookclub), null, false, 
        (res) => {
            club = res.bookclub;
            loaded.value = true;
        },
        (err) => {
            error = err;
            loaded.value = true;
        }
    );
}


/**
 * ----------------------------------------------------------------------------
 * @end_of_functions
 * ----------------------------------------------------------------------------
 */

/**
 * ----------------------------------------------------------------------------
 * @load
 * ----------------------------------------------------------------------------
 */

 loadBookClub();

/**
 * ----------------------------------------------------------------------------
 * @load
 * ----------------------------------------------------------------------------
 */
</script>